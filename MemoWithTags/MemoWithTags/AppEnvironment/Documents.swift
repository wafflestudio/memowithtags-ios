2. 이용자의 귀책사유로 인하여 승인이 불가능하거나 기타 규정한 제반 사항을 위반하며 신청하는 경우
③ 회원이 제3자 계정을 등록하는 것은 Memo with Tags가 해당 계정의 개인정보에 접근할 수 있고 제9조와 본 약관에 명시된 범주 내에서 그 정보를 이용할 수 있는 것으로 간주한다.
④ Memo with Tags는 서비스 관련 설비의 여유가 없거나, 기술상 또는 업무상 문제가 있는 경우에는 이용 신청 승낙을 유보할 수 있다.

제4조(회원의 탈퇴와 자격 상실)
① 회원은 Memo with Tags에 언제든지 자신의 등록을 말소해 줄 것(회원 탈퇴)을 요청할 수 있으며 Memo with Tags는 위 요청을 받은 즉시 해당 이용자의 회원 등록 말소를 위한 절차를 밟아야 한다.
② 회원이 다음 각 호의 사유에 해당하는 경우, Memo with Tags는 회원 자격을 적절한 방법으로 제한 및 정지, 상실시킬 수 있다.
1. 다른 사람의 "서비스" 이용을 방해하거나 그 정보를 도용하는 등 인터넷 통신질서를 위협하는 경우
2. "서비스"를 이용하여 본 약관이 금지하거나 공서양속에 반하는 행위를 하는 경우

제5조(회원에 대한 통지 및 정보제공)
① Memo with Tags가 특정 혹은 불특정 회원에 대한 통지를 하는 경우 알림을 이용한다.
② Memo with Tags는 회원이 서비스 이용에 필요하다고 인정되는 다양한 정보를 회원이 제공한 전자우편 주소로 제공할 수 있다.
③ Memo with Tags는 다음 각 호에 해당하는 경우 회원의 동의 여부와 상관없이 전자우편으로 발송할 수 있다.
- 이용 신청에서 입력한 전자우편 주소의 소유를 확인하기 위해서 인증메일을 발송하는 경우
- 회원의 정보가 변경되는 것을 확인하기 위해서 인증메일을 발송하는 경우
- 기타 서비스를 제공함에 있어 회원이 반드시 알아야 하는 중대한 정보라고 Memo with Tags가 판단하는 경우

제3장 서비스
제6조(서비스의 제공)
Memo with Tags는 회원에게 아래와 같은 서비스를 제공한다.
1. 메모 작성 및 태그 분류 기능
2. 태그 추천, 메모 추천 기능(추후 AI 기능 추가 예정)
3. 기타 Memo with Tags가 자체 개발하거나 다른 회사와의 협력 계약 등을 통해 회원들에게 제공할 일체의 서비스
① AI 기능 관련 사항:
1. AI 기능은 베타 서비스로 제공될 수 있으며, 정확성이나 완성도를 보장하지 않는다.
2. AI가 생성하는 콘텐츠의 저작권은 Memo with Tags에 귀속된다.
3. AI 기능 사용 시 발생하는 문제에 대해 Memo with Tags는 책임을 지지 않는다.

제7조(서비스의 중단)
① Memo with Tags는 컴퓨터 등 정보통신설비의 보수점검·교체 및 고장, 통신의 두절 등의 경우에는 서비스의 제공을 일시적으로 중단할 수 있고, 새로운 서비스로의 교체 및 기타 Memo with Tags가 적절하다고 판단하는 경우 현재 제공되는 서비스를 완전히 중단할 수 있다.
② 제1항에 의한 서비스 중단의 경우 Memo with Tags는 제5조에서 정한 방법으로 회원에게 통지한다. 단, Memo with Tags가 통제할 수 없는 사유로 인한 서비스의 중단(시스템 관리자의 고의 또는 과실이 없는 디스크 장애, 시스템 다운, 천재지변 등)으로 인하여 사전 통지가 불가능한 경우는 예외로 한다.
③ Memo with Tags가 서비스 운영을 중단할 경우, 다음 각 호의 조치를 취한다:
1. 서비스 중단 30일 전까지 회원에게 통지한다.
2. 회원의 메모와 태그 데이터를 JSON, CSV 등 범용 데이터 내보내기 형식으로 제공한다.
3. 서비스 중단 후 30일간 데이터 내보내기 기능을 유지한다.
4. 데이터 이전 및 내보내기 과정에서 발생하는 기술적 문제에 대해 Memo with Tags는 적절한 기술 지원을 제공한다.

제4장 권리와 의무
제8조(이용자의 개인정보보호)
Memo with Tags는 회원의 등록정보 및 메모를 포함한 회원들의 개인정보를 보호하기 위하여 노력한다. 회원의 개인정보보호에 관해서는 Memo with Tags가 별도로 정하는 "개인정보처리방침"에 정한 바에 의한다.

제9조(Memo with Tags의 의무)
① Memo with Tags는 법령과 본 약관이 금지하거나 공서양속에 반하는 행위를 하지 않으며 본 약관이 정하는 바에 따라 지속적이고, 안정적으로 서비스를 제공하기 위해서 노력한다.
② Memo with Tags는 이용자가 안전하게 인터넷 서비스를 이용할 수 있도록 이용자의 개인정보(신용정보 포함) 보호를 위한 보안 시스템을 구축한다.

제10조(광고 및 제휴)
① Memo with Tags는 회원들의 이용에 불편함이 없는 선에서 영리 및 비영리 목적의 광고를 게시할 수 있고 그 수익을 Memo with Tags의 운영비로 충당할 수 있다.
② Memo with Tags는 더 나은 서비스 제공을 위하여 다른 회사와 제휴할 수 있다.
③ Memo with Tags는 이용자의 이용 기록을 활용한 광고를 게재할 수 있다.

제11조(이용자의 ID 및 비밀번호에 대한 의무)
① Memo with Tags가 관계법령, "개인정보처리방침"에 의해서 그 책임을 지는 경우를 제외하고, 자신의 ID와 비밀번호에 관한 관리책임은 각 회원에게 있다.
② 회원은 자신의 ID 및 비밀번호를 제3자가 이용하게 해서는 안 된다.
③ 회원은 자신의 ID 및 비밀번호를 도난당하거나 제3자가 사용하고 있음을 인지한 경우에는 바로 Memo with Tags에 통보하고 Memo with Tags의 안내가 있는 경우에는 그에 따라야 한다.

제12조(회원의 의무)
① 회원은 다음 각 호의 행위를 하여서는 안 된다.
1. 개인정보 또는 계정 기만, 침해, 공유 행위
- 다른 이용자에 대한 개인정보를 동의 없이 수집, 저장, 공개하는 행위
- Memo with Tags 및 기타 제3자의 인격권 또는 지적재산권을 침해하거나 업무를 방해하는 행위
- 개인정보를 허위, 누락, 오기, 도용하여 작성하는 행위
2. 업무 방해 행위
- 허가하지 않은 방식의 서비스 이용 행위
- Memo with Tags의 대표나 Memo with Tags 서비스의 관리자를 가장하여 글을 게시하거나 메일을 발송하는 행위 
- 컴퓨터 소프트웨어, 하드웨어, 전기통신 장비의 정상적인 가동을 방해, 파괴할 목적으로 고안된 소프트웨어 바이러스, 기타 다른 컴퓨터 코드, 파일, 프로그램을 포함하고 있는 자료를 게시하는 행위
3. 시스템 부정행위
- Memo with Tags의 서비스를 이용하여 영리목적의 활동을 하는 행위
- 기타 Memo with Tags가 제공하는 서비스에 정한 약관을 위반하는 행위
4. 이 약관,개인정보 처리방침에서 이행 및 비이행을 명시한 내용에 반하는 행위
5. 기타 현행법에 어긋나거나 부적절하다고 판단되는 행위
② 제1항에 해당하는 행위를 한 회원이 있을 경우 Memo with Tags는 회원의 회원 자격을 제5조에 따라 제한, 정지 또는 상실시킬 수 있다.
- 회원의 서비스 이용 권한, 자격 제한 및 회수
- 회원과 체결된 이용계약을 회원의 동의나 통보 없이 파기
- 회원의 메모를 임의로 접근 제한, 삭제
- 그 외 필요하다고 판단되는 조치
③ Memo with Tags는 1항부터 전항까지로 인해 발생한 피해에 대해 어떠한 책임을 지지 않으며, 회원은 귀책사유로 인해 발생한 모든 손해를 배상할 책임이 있다.

제13조(메모 및 태그의 권리와 책임)
① 회원은 Memo with Tags를 통해 얻은 정보를 Memo with Tags의 사전 승인 없이 복제, 전송, 출판, 배포, 방송 등 영리 목적으로 이용하거나 제3자에게 제공할 수 없다.
② 회원은 자신이 서비스 내에 작성한 메모와 태그를 Memo with Tags가 다음과 같은 목적으로 사용하는 것을 허락한다.
- 회원의 편의를 위해 메모와 태그를 이미지 형태로 변환하는 것
- 회원이 허용한 다른 회원에게 메모를 공개하는 것
- 회원이 요청한 경우 제3자 서비스에 제공하는 것
- 비영리적 목적으로 메모와 태그를 개인 식별이 불가능한 형태로 가공, 공개 및 이용하는 것
③ Memo with Tags는 관련 법률에 따라 회원의 메모와 태그로 인해 법적 문제가 발생하는 경우, 해당 메모에 대한 접근을 일시적으로 제한하거나 삭제할 수 있다.

제14조(저작권의 귀속)
① Memo with Tags 서비스에 대한 저작권 및 지적재산권은 Memo with Tags에 귀속된다.
② 회원이 작성한 메모와 태그에 대한 권리는 회원에게 귀속된다.
③ 저작권법에 따라 데이터베이스 제작자는 복제권 및 전송권을 포함한 데이터베이스 전부에 대한 권리를 가지고 있으며, 이는 법률에 따라 보호를 받는 대상이다. 그러므로 이용자는 데이터베이스 제작자인 Memo with Tags의 승인 없이 데이터베이스의 전부 또는 일부를 복제, 배포, 방송 또는 전송할 수 없다.

