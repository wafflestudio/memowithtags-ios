Memo with Tags 이용약관

제1장 총칙
제1조(목적)
본 약관은 Memo with Tags 팀(이하 "운영자")이 제공하는 개인 메모 관리 서비스 Memo with Tags의 이용과 관련하여, 회원과 운영자 간의 권리·의무 및 책임사항, 기타 필요한 사항을 규정함을 목적으로 한다.

